# **** Packages in python **** #


# A package is a collection of module organied in directories(folder) with an  __init__.pyfile. It allows you to structure your Python projects logically.

# Why use packages
# .To group related modules together.
# .To create larger applications or libaries.

# structure Example

# my_package/
#    __init__.py
#    math_utils.py
#    string_utils.py

# Use the package:
# Syntax : from my_package import <package_name>.
# Example : from my_package import math_utils, string_utils
